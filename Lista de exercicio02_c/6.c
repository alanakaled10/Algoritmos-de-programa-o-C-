/*Aprovado ou Reprovado*/

#include <stdio.h>
#include <math.h>
int main(){
    
    float nota;
    printf("Digite a nota do aluno (0-10): ");
    scanf("%f", &nota);

    if (nota <= 7)
    {
        printf("Aluno reprovado!");
    }
    else
    {
        printf("Aluno reprovado!");
    }
    
    
    return 0;
}